// includes ()
// this method is used for to check that the given number is present or include or not in the given array.
// if present then it will return the true or if not so, it will return the false.

const num = [1, 2, 3, 4, 5];

console.log(num.includes(3));  // true

const ans = num.includes(6);  
console.log(ans);  //false